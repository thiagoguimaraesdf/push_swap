#include <string.h>
#include <stdlib.h>
#include "../include/libft.h"

void	ft_lstadd_back(t_list **lst, t_list *new)
{
	t_list	*last;

	if (lst)
	{
		if (lst[0] == NULL)
			lst[0] = new;
		else
		{
			last = ft_lstlast(lst[0]);
			last->next = new;
		}
	}
}
